package com.forezp.entity;

public class ProblemList {
	private String discoverTime;
	private String target;
	private String introducer;
	private String problemSketch;
	private String finishTime;
	private String responsibleField;
	private String responsiblePerson;
	private String status;
	private String attachList;
	
	public String getDiscoverTime() {
		return discoverTime;
	}
	public void setDiscoverTime(String discoverTime) {
		this.discoverTime = discoverTime;
	}
	public String getTarget() {
		return target;
	}
	public void setTarget(String target) {
		this.target = target;
	}
	public String getIntroducer() {
		return introducer;
	}
	public void setIntroducer(String introducer) {
		this.introducer = introducer;
	}
	public String getProblemSketch() {
		return problemSketch;
	}
	public void setProblemSketch(String problemSketch) {
		this.problemSketch = problemSketch;
	}
	public String getFinishTime() {
		return finishTime;
	}
	public void setFinishTime(String finishTime) {
		this.finishTime = finishTime;
	}
	public String getResponsibleField() {
		return responsibleField;
	}
	public void setResponsibleField(String responsibleField) {
		this.responsibleField = responsibleField;
	}
	public String getResponsiblePerson() {
		return responsiblePerson;
	}
	public void setResponsiblePerson(String responsiblePerson) {
		this.responsiblePerson = responsiblePerson;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAttachList() {
		return attachList;
	}
	public void setAttachList(String attachList) {
		this.attachList = attachList;
	}
	
}
